package business;

/**
 * Représente la couleur d'un jeton ou d'un joueur dans le jeu Puissance 4.
 * Cette énumération est utilisée pour identifier à qui appartient un jeton
 * ou quel est le camp d'un joueur.
 */
public enum Couleur {
    /**
     * Représente la couleur jaune, associé au joueur jaune.
     */
    JAUNE,
    /**
     * Représente la couleur rouge, associé au joueur rouge.
     */
    ROUGE;

}
